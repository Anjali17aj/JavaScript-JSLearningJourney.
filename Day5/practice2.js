//Array methods shift , unshifts
let months = ["January","July","March","August"];
console.log(months);
let newMonths = months.shift();
console.log(newMonths);
console.log(months);
let newMonths1 = months.shift();
console.log(newMonths1);
console.log(months);
let newMonths2 = months.unshift("June");
console.log(newMonths2);
console.log(months);
let newMonths3 = months.unshift("July");
console.log(newMonths3);
console.log(months);



